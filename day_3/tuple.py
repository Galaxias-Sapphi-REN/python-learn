# 脚本操作符
def script_operator():
    """
---------------------------------------------------------------------------------------------------
    例：打印常用的tuple脚本操作符
---------------------------------------------------------------------------------------------------
    与字符串一样，元组之间可以使用 + 号和 * 号进行运算。这就意味着他们可以组合和复制，运算后会生成一个新的元组。
---------------------------------------------------------------------------------------------------
    Python 表达式	                    结果	                                描述
    len((1, 2, 3))	                3	                                计算元素个数
    (1, 2, 3) + (4, 5, 6)	        (1, 2, 3, 4, 5, 6)	                连接
    ('Hi!',) * 4	                ('Hi!', 'Hi!', 'Hi!', 'Hi!')	    复制
    3 in (1, 2, 3)	                True	                            元素是否存在
    for x in (1, 2, 3): print x,	1 2 3	                            迭代
---------------------------------------------------------------------------------------------------
    :return:
    """
    print('len((1, 2, 3))               : ' + str(len((1, 2, 3))))
    print('(1, 2, 3) + (4, 5, 6)        : ' + str((1, 2, 3) + (4, 5, 6)))
    print('(\'Hi!\') * 4)               : ' + str(('Hi!',) * 4))
    print('for x in (1, 2, 3): print(x) : ')
    for x in [1, 2, 3]:
        print(x)


# 内建函数和列表函数
def built_in_and_tuple_function():
    """
---------------------------------------------------------------------------------------------------
    例：常用的tuple内建函数和列表函数
---------------------------------------------------------------------------------------------------
    :return:
    """
    tuple1 = ('runoob', 786, 2.23, 'john', 70.2)
    tuple2 = (123, 'john')

    # 任意无符号的对象，以逗号隔开，默认为元组
    print('abc', -4.24e93, 18 + 6.6j, 'xyz')
    print(tuple1)  # 输出完整元组
    print(tuple1[0])  # 输出元组的第一个元素
    print(tuple1[1:3])  # 输出第二个至第三个的元素
    print(tuple1[2:])  # 输出从第三个开始至列表末尾的所有元素
    print(tuple2 * 2)  # 输出元组两次
    print(tuple1 + tuple2)  # 打印组合的元组

    print(cmp((1,), (2,)))
    print(len((1, 2)))
    print(max((1, 2)))
    print(min((1, 2)))
    print(tuple([1, 2]))


